/*
|--------------------------------------------------------------------------
| Get User by Email
|--------------------------------------------------------------------------
*/

export async function getUserByEmail(email) {
  if (!email) {
    return null;
  }

  const normalizedEmail = String(email)
    .trim()
    .toLowerCase();

  const { data, error } = await supabase
    .from("users")
    .select(`
      id,
      name,
      email,
      username,
      password_hash,
      role,
      status,
      created_at,
      updated_at
    `)
    .ilike("email", normalizedEmail)
    .maybeSingle();

  if (error) {
    throw new Error(
      `Failed to retrieve user: ${error.message}`,
    );
  }

  return data;
}

