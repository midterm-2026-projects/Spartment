/*
|--------------------------------------------------------------------------
| Create User
|--------------------------------------------------------------------------
| passwordHash must already be generated by the service.
|--------------------------------------------------------------------------
*/

export async function createUserRecord({
  name,
  email,
  username = null,
  passwordHash,
  role = "tenant",
  status = "Active",
}) {
  const { data, error } = await supabase
    .from("users")
    .insert({
      name: String(name).trim(),
      email: String(email).trim().toLowerCase(),
      username: username
        ? String(username).trim()
        : null,
      password_hash: passwordHash,
      role,
      status,
    })
    .select(`
      id,
      name,
      email,
      username,
      role,
      status,
      created_at,
      updated_at
    `)
    .single();

  if (error) {
    if (error.code === "23505") {
      throw new Error(
        "A user with this email or username already exists",
      );
    }

    throw new Error(
      `Failed to create user: ${error.message}`,
    );
  }

  return data;
}

/*
|--------------------------------------------------------------------------
| Update User Password Hash
|--------------------------------------------------------------------------
*/

export async function updateUserPasswordHash(
  userId,
  passwordHash,
) {
  const { data, error } = await supabase
    .from("users")
    .update({
      password_hash: passwordHash,
    })
    .eq("id", userId)
    .select(`
      id,
      name,
      email,
      username,
      role,
      status,
      updated_at
    `)
    .maybeSingle();

  if (error) {
    throw new Error(
      `Failed to update password: ${error.message}`,
    );
  }

  return data;
}